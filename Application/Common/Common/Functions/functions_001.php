        case 1:$info = '<a href="javascript:unrecommend(' . $id . ')">取消推荐</a>';
            break;
    }
    return $info;
}

/* 组名 */

function getGroupName($id) {
    if ($id == 0) {
        return '无上级组';
    }
    if ($list = F('groupName')) {
        return $list[$id];
    }
    $dao = D("Role");
    $list = $dao->field('id,name')->select();
    foreach ($list as $vo) {
        $nameList[$vo['id']] = $vo['name'];
    }
    $name = $nameList[$id];
    F('groupName', $nameList);
    return $name;
}

